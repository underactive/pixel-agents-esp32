void OfficeState::update(float dt) {
    for (int i = 0; i < MAX_AGENTS; i++) {
        if (!_chars[i].alive) continue;
        updateCharacter(_chars[i], dt);
    }
    updatePet(dt);
}

void OfficeState::updateCharacter(Character& ch, float dt) {
    ch.frameTimer += dt;

    // Handle spawn/despawn effects (only used at boot if desired)
    if (ch.state == CharState::SPAWN) {
        ch.effectTimer += dt;
        if (ch.effectTimer >= SPAWN_DURATION_SEC) {
            ch.state = CharState::IDLE;
            ch.frame = 0;
            ch.frameTimer = 0;
        }
        return;
    }

    if (ch.state == CharState::DESPAWN) {
        ch.effectTimer += dt;
        if (ch.effectTimer >= SPAWN_DURATION_SEC) {
            // Characters never truly despawn -- transition to idle in zone
            ch.state = CharState::IDLE;
            ch.frame = 0;
            ch.frameTimer = 0;
        }
        return;
    }

    switch (ch.state) {
        case CharState::TYPE:
        case CharState::READ: {
            if (ch.frameTimer >= TYPE_FRAME_DURATION_SEC) {
                ch.frameTimer -= TYPE_FRAME_DURATION_SEC;
                ch.frame = (ch.frame + 1) % 2;
            }
            if (!ch.isActive) {
                ch.state = CharState::IDLE;
                ch.frame = 0;
                ch.frameTimer = 0;
                ch.bubbleType = 0;
                ch.bubbleTimer = 0;
                ch.seatIdx = -1;
                walkToZone(ch);
            }
            break;
        }

        case CharState::IDLE: {
            ch.frame = 0;

            // If became active, go to seat
            if (ch.isActive && ch.seatIdx >= 0) {
                const auto& ws = WORKSTATIONS[ch.seatIdx];
                if (ch.tileCol == ws.seatCol && ch.tileRow == ws.seatRow) {
                    ch.state = CharState::TYPE;
                    ch.dir = ws.facingDir;
                } else {
                    startWalk(ch, (int8_t)ws.seatCol, (int8_t)ws.seatRow);
                }
                ch.frame = 0;
                ch.frameTimer = 0;
                break;
            }

            // Wander timer
            ch.wanderTimer -= dt;
            if (ch.wanderTimer <= 0) {
                // Unassigned characters wander within their home zone
                if (ch.agentId < 0) {
                    startZoneWander(ch);
                } else {
                    startWander(ch);
                }
                ch.wanderTimer = randomRange(WANDER_PAUSE_MIN_SEC, WANDER_PAUSE_MAX_SEC);
            }
            break;
        }

        case CharState::WALK: {
            // Walk animation
            if (ch.frameTimer >= WALK_FRAME_DURATION_SEC) {
                ch.frameTimer -= WALK_FRAME_DURATION_SEC;
                ch.frame = (ch.frame + 1) % 4;
            }

            if (ch.pathIdx >= ch.pathLen) {
                // Path complete
                float cx = ch.tileCol * TILE_SIZE + TILE_SIZE / 2.0f;
                float cy = ch.tileRow * TILE_SIZE + TILE_SIZE / 2.0f;
                ch.x = cx;
                ch.y = cy;

                if (ch.isActive && ch.seatIdx >= 0) {
                    const auto& ws = WORKSTATIONS[ch.seatIdx];
                    if (ch.tileCol == ws.seatCol && ch.tileRow == ws.seatRow) {
                        ch.state = isReadingTool(ch.toolName) ? CharState::READ : CharState::TYPE;
                        ch.dir = ws.facingDir;
                    } else {
                        ch.state = CharState::IDLE;
                    }
                } else {
                    ch.state = CharState::IDLE;
                    ch.wanderTimer = randomRange(WANDER_PAUSE_MIN_SEC, WANDER_PAUSE_MAX_SEC);
                }
                ch.frame = 0;
                ch.frameTimer = 0;
                break;
            }

            // Move toward next tile
            PathNode next = ch.path[ch.pathIdx];

            // Update direction
            int dc = next.col - ch.tileCol;
            int dr = next.row - ch.tileRow;
            if (dc > 0) ch.dir = Dir::RIGHT;
            else if (dc < 0) ch.dir = Dir::LEFT;
            else if (dr > 0) ch.dir = Dir::DOWN;
            else ch.dir = Dir::UP;

            ch.moveProgress += (WALK_SPEED_PX_PER_SEC / TILE_SIZE) * dt;

            float fromX = ch.tileCol * TILE_SIZE + TILE_SIZE / 2.0f;
            float fromY = ch.tileRow * TILE_SIZE + TILE_SIZE / 2.0f;
            float toX = next.col * TILE_SIZE + TILE_SIZE / 2.0f;
            float toY = next.row * TILE_SIZE + TILE_SIZE / 2.0f;

            float t = ch.moveProgress;
            if (t > 1.0f) t = 1.0f;
            ch.x = fromX + (toX - fromX) * t;
            ch.y = fromY + (toY - fromY) * t;

            if (ch.moveProgress >= 1.0f) {
                ch.tileCol = next.col;
                ch.tileRow = next.row;
                ch.x = toX;
                ch.y = toY;
                ch.pathIdx++;
                ch.moveProgress = 0;
            }
            break;
        }

        default:
            break;
    }

    // Tick bubble timer
    if (ch.bubbleType > 0 && ch.bubbleTimer > 0) {
        ch.bubbleTimer -= dt;
        if (ch.bubbleTimer <= 0) {
            ch.bubbleType = 0;
            ch.bubbleTimer = 0;
        }
    }
}

void OfficeState::startWalk(Character& ch, int8_t goalCol, int8_t goalRow) {
    PathNode pathBuf[64];
    uint8_t pathLen = 0;
    if (findPath(ch.tileCol, ch.tileRow, goalCol, goalRow, pathBuf, pathLen)) {
        memcpy(ch.path, pathBuf, pathLen * sizeof(PathNode));
        ch.pathLen = pathLen;
        ch.pathIdx = 0;
        ch.moveProgress = 0;
        ch.state = CharState::WALK;
        ch.frame = 0;
        ch.frameTimer = 0;
    }
}

void OfficeState::startWander(Character& ch) {
    // Pick a random walkable tile anywhere
    for (int attempt = 0; attempt < 20; attempt++) {
        int8_t col = (int8_t)randomInt(1, GRID_COLS - 2);
        int8_t row = (int8_t)randomInt(1, GRID_ROWS - 1);
        if (isWalkable(col, row)) {
            startWalk(ch, col, row);
            ch.wanderCount++;
            return;
        }
    }
}

void OfficeState::startZoneWander(Character& ch) {
    int colMin, colMax, rowMin, rowMax;
    if (ch.homeZone == SocialZone::BREAK_ROOM) {
        colMin = ZONE_BREAK_COL_MIN; colMax = ZONE_BREAK_COL_MAX;
        rowMin = ZONE_BREAK_ROW_MIN; rowMax = ZONE_BREAK_ROW_MAX;
    } else {
        colMin = ZONE_LIB_COL_MIN; colMax = ZONE_LIB_COL_MAX;
        rowMin = ZONE_LIB_ROW_MIN; rowMax = ZONE_LIB_ROW_MAX;
    }

    for (int attempt = 0; attempt < 30; attempt++) {
        int8_t col = (int8_t)randomInt(colMin, colMax);
        int8_t row = (int8_t)randomInt(rowMin, rowMax);
        if (isWalkable(col, row)) {
            startWalk(ch, col, row);
            ch.wanderCount++;
            return;
        }
    }
}

void OfficeState::walkToZone(Character& ch) {
    int colMin, colMax, rowMin, rowMax;
    if (ch.homeZone == SocialZone::BREAK_ROOM) {
        colMin = ZONE_BREAK_COL_MIN; colMax = ZONE_BREAK_COL_MAX;
        rowMin = ZONE_BREAK_ROW_MIN; rowMax = ZONE_BREAK_ROW_MAX;
    } else {
        colMin = ZONE_LIB_COL_MIN; colMax = ZONE_LIB_COL_MAX;
        rowMin = ZONE_LIB_ROW_MIN; rowMax = ZONE_LIB_ROW_MAX;
    }

    // Try random tiles in zone
    for (int attempt = 0; attempt < 30; attempt++) {
        int8_t col = (int8_t)randomInt(colMin, colMax);
        int8_t row = (int8_t)randomInt(rowMin, rowMax);
        if (isWalkable(col, row)) {
            startWalk(ch, col, row);
            return;
        }
    }

    // Fallback: scan zone for any walkable tile
    for (int r = rowMin; r <= rowMax; r++) {
        for (int c = colMin; c <= colMax; c++) {
            if (isWalkable((int8_t)c, (int8_t)r)) {
                startWalk(ch, (int8_t)c, (int8_t)r);
                return;
            }
        }
    }

    // If no walkable tile in zone, just go idle where we are
    ch.state = CharState::IDLE;
    ch.frame = 0;
    ch.frameTimer = 0;
    ch.wanderTimer = randomRange(WANDER_PAUSE_MIN_SEC, WANDER_PAUSE_MAX_SEC);
}

void OfficeState::snapToSeat(Character& ch) {
    if (ch.seatIdx < 0) return;
    const auto& ws = WORKSTATIONS[ch.seatIdx];
    ch.tileCol = (int8_t)ws.seatCol;
    ch.tileRow = (int8_t)ws.seatRow;
    ch.x = ws.seatCol * TILE_SIZE + TILE_SIZE / 2.0f;
    ch.y = ws.seatRow * TILE_SIZE + TILE_SIZE / 2.0f;
    ch.dir = ws.facingDir;
}

bool OfficeState::isWalkable(int8_t col, int8_t row) const {
    if (col < 0 || col >= GRID_COLS || row < 0 || row >= GRID_ROWS) return false;
    return _tiles[row][col] == TileType::FLOOR;
}

// BFS pathfinding
bool OfficeState::findPath(int8_t fromCol, int8_t fromRow, int8_t toCol, int8_t toRow,
                           PathNode* outPath, uint8_t& outLen) {
    if (fromCol == toCol && fromRow == toRow) {
        outLen = 0;
        return true;
    }

    // BFS with parent tracking
    static const int8_t DX[] = {0, 0, 1, -1};
    static const int8_t DY[] = {1, -1, 0, 0};

    bool visited[GRID_ROWS][GRID_COLS];
    int8_t parentCol[GRID_ROWS][GRID_COLS];
    int8_t parentRow[GRID_ROWS][GRID_COLS];
    memset(visited, 0, sizeof(visited));
    memset(parentCol, -1, sizeof(parentCol));
    memset(parentRow, -1, sizeof(parentRow));

    // BFS queue (fixed size)
    struct QNode { int8_t col, row; };
    QNode queue[GRID_ROWS * GRID_COLS];
    int qHead = 0, qTail = 0;

    visited[fromRow][fromCol] = true;
    queue[qTail++] = {fromCol, fromRow};

    bool found = false;
    while (qHead < qTail) {
        QNode cur = queue[qHead++];

        if (cur.col == toCol && cur.row == toRow) {
            found = true;
            break;
        }

        for (int d = 0; d < 4; d++) {
            int8_t nc = cur.col + DX[d];
            int8_t nr = cur.row + DY[d];
            if (nc < 0 || nc >= GRID_COLS || nr < 0 || nr >= GRID_ROWS) continue;
            if (visited[nr][nc]) continue;
            // Allow walking to destination even if it's "blocked" (e.g., seat tile)
            if (!isWalkable(nc, nr) && !(nc == toCol && nr == toRow)) continue;
            if (qTail >= GRID_ROWS * GRID_COLS) break;
            visited[nr][nc] = true;
            parentCol[nr][nc] = cur.col;
            parentRow[nr][nc] = cur.row;
            queue[qTail++] = {nc, nr};
        }
    }

    if (!found) {
        outLen = 0;
        return false;
    }

    // Reconstruct path (reverse)
    PathNode revPath[64];
    int len = 0;
    int8_t cc = toCol, cr = toRow;
    while (cc != fromCol || cr != fromRow) {
        if (len >= 64) { outLen = 0; return false; }
        revPath[len++] = {cc, cr};
        int8_t pc = parentCol[cr][cc];
        int8_t pr = parentRow[cr][cc];
        cc = pc;
        cr = pr;
    }

    // Reverse into output
    outLen = (uint8_t)len;
    for (int i = 0; i < len; i++) {
        outPath[i] = revPath[len - 1 - i];
    }
    return true;
}

float OfficeState::randomRange(float minVal, float maxVal) {
    return minVal + ((float)random(10000) / 10000.0f) * (maxVal - minVal);
}

int OfficeState::randomInt(int minVal, int maxVal) {
    return minVal + random(maxVal - minVal + 1);
}

// ── Pet (French Bulldog) ─────────────────────────────────

void OfficeState::initPet() {
    memset(&_pet, 0, sizeof(Pet));
    _pet.dir = Dir::DOWN;
    _pet.behavior = DogBehavior::WANDER;
    _pet.phaseTimer = DOG_WANDER_DURATION_SEC;
    _pet.napTimer = DOG_NAP_INTERVAL_SEC;
    _pet.targetPickTimer = DOG_PICK_TARGET_SEC;
    _pet.followTarget = -1;
    _pet.lastTargetCol = -1;
    _pet.lastTargetRow = -1;
    _pet.wanderTimer = randomRange(DOG_WANDER_PAUSE_MIN_SEC, DOG_WANDER_PAUSE_MAX_SEC);
    _pet.blinkTimer = randomRange(DOG_BLINK_MIN_SEC, DOG_BLINK_MAX_SEC);
    _pet.tailWagCooldown = 0;

    // Place at a random walkable tile
    for (int attempt = 0; attempt < 40; attempt++) {
        int8_t col = (int8_t)randomInt(2, GRID_COLS - 2);
        int8_t row = (int8_t)randomInt(2, GRID_ROWS - 2);
        if (isWalkable(col, row)) {
            _pet.tileCol = col;
            _pet.tileRow = row;
            _pet.x = col * TILE_SIZE + TILE_SIZE / 2.0f;
            _pet.y = row * TILE_SIZE + TILE_SIZE / 2.0f;
            return;
        }
    }
    // Fallback
    _pet.tileCol = 5;
    _pet.tileRow = 3;
    _pet.x = 5 * TILE_SIZE + TILE_SIZE / 2.0f;
    _pet.y = 3 * TILE_SIZE + TILE_SIZE / 2.0f;
}

void OfficeState::petStartWalk(int8_t goalCol, int8_t goalRow) {
    PathNode pathBuf[64];
    uint8_t pathLen = 0;
    if (findPath(_pet.tileCol, _pet.tileRow, goalCol, goalRow, pathBuf, pathLen)) {
        memcpy(_pet.path, pathBuf, pathLen * sizeof(PathNode));
        _pet.pathLen = pathLen;
        _pet.pathIdx = 0;
        _pet.moveProgress = 0;
        _pet.walking = true;
        _pet.frame = 0;
        _pet.frameTimer = 0;
    }
}

void OfficeState::petWander() {
    for (int attempt = 0; attempt < 20; attempt++) {
        int8_t col = (int8_t)randomInt(1, GRID_COLS - 2);
        int8_t row = (int8_t)randomInt(1, GRID_ROWS - 1);
        if (isWalkable(col, row)) {
            petStartWalk(col, row);
            return;
        }
    }
}

void OfficeState::petPickTarget() {
    // Pick a random alive character
    int candidates[MAX_AGENTS];
    int count = 0;
    for (int i = 0; i < MAX_AGENTS; i++) {
        if (_chars[i].alive) candidates[count++] = i;
    }
    if (count > 0) {
        _pet.followTarget = (int8_t)candidates[randomInt(0, count - 1)];
        _pet.lastTargetCol = -1;
        _pet.lastTargetRow = -1;
    }
}

void OfficeState::petFollowNear() {
    if (_pet.followTarget < 0 || _pet.followTarget >= MAX_AGENTS) return;
    const Character& target = _chars[_pet.followTarget];
    if (!target.alive) {
        petPickTarget();
        return;
    }

    int8_t tCol = target.tileCol;
    int8_t tRow = target.tileRow;

    // Hysteresis: only re-pathfind if target moved significantly
    if (_pet.lastTargetCol >= 0) {
        int dx = abs(tCol - _pet.lastTargetCol);
        int dy = abs(tRow - _pet.lastTargetRow);
        if (dx + dy <= DOG_FOLLOW_HYSTERESIS) return;
    }

    // Already close enough?
    int distX = abs(_pet.tileCol - tCol);
    int distY = abs(_pet.tileRow - tRow);
    if (distX + distY <= 2) return;

    // Pick a walkable tile within DOG_FOLLOW_RADIUS of target
    for (int attempt = 0; attempt < 30; attempt++) {
        int8_t col = (int8_t)(tCol + randomInt(-DOG_FOLLOW_RADIUS, DOG_FOLLOW_RADIUS));
        int8_t row = (int8_t)(tRow + randomInt(-DOG_FOLLOW_RADIUS, DOG_FOLLOW_RADIUS));
        if (col < 0 || col >= GRID_COLS || row < 0 || row >= GRID_ROWS) continue;
        int d = abs(col - tCol) + abs(row - tRow);
        if (d < 2 || d > DOG_FOLLOW_RADIUS) continue;
        if (isWalkable(col, row)) {
            petStartWalk(col, row);
            _pet.lastTargetCol = tCol;
            _pet.lastTargetRow = tRow;
            return;
        }
    }
}

void OfficeState::updatePet(float dt) {
    _pet.frameTimer += dt;

    // ── Nap timer (always counts down) ──
    _pet.napTimer -= dt;
    if (_pet.behavior != DogBehavior::NAP && _pet.napTimer <= 0) {
        _pet.behavior = DogBehavior::NAP;
        _pet.napRemaining = DOG_NAP_DURATION_SEC;
        _pet.walking = false;
        _pet.pathLen = 0;
        _pet.frame = 0;
        _pet.frameTimer = 0;
        _pet.napZTimer = DOG_NAP_Z_TOGGLE_SEC;
        _pet.showingZ = false;
        _pet.isSitting = false;
        _pet.isHappy = false;
        _pet.isBlinking = false;
    }

    // ── Tail wag cooldown (always ticks) ──
    if (_pet.tailWagCooldown > 0) _pet.tailWagCooldown -= dt;

    // ── Tail wag proximity check ──
    if (_pet.tailWagTimer <= 0 && _pet.tailWagCooldown <= 0 &&
        _pet.behavior != DogBehavior::NAP) {
        for (int i = 0; i < MAX_AGENTS; i++) {
            if (!_chars[i].alive) continue;
            int dx = abs(_pet.tileCol - _chars[i].tileCol);
            int dy = abs(_pet.tileRow - _chars[i].tileRow);
            if (dx + dy <= DOG_TAIL_WAG_PROXIMITY) {
                _pet.tailWagTimer = DOG_TAIL_WAG_DURATION_SEC;
                _pet.tailFrameTimer = 0;
                _pet.tailFrame = 0;
                break;
            }
        }
    }

    // ── Tail wag animation ──
    if (_pet.tailWagTimer > 0) {
        _pet.tailWagTimer -= dt;
        _pet.tailFrameTimer += dt;
        if (_pet.tailFrameTimer >= DOG_TAIL_FRAME_SEC) {
            _pet.tailFrameTimer -= DOG_TAIL_FRAME_SEC;
            _pet.tailFrame = (_pet.tailFrame + 1) % 2;
        }
        if (_pet.tailWagTimer <= 0) {
            _pet.tailWagTimer = 0;
            _pet.tailWagCooldown = DOG_TAIL_WAG_COOLDOWN_SEC;
        }
    }

    // ── Idle animations (blink, happy, breathing) ──
    if (!_pet.walking && _pet.behavior != DogBehavior::NAP) {
        // Breathing cycle
        _pet.idleFrameTimer += dt;
        if (_pet.idleFrameTimer >= DOG_IDLE_FRAME_SEC) {
            _pet.idleFrameTimer -= DOG_IDLE_FRAME_SEC;
            _pet.idleFrame = (_pet.idleFrame + 1) % 2;
        }

        // Blink
        if (_pet.isBlinking) {
            _pet.blinkRemaining -= dt;
            if (_pet.blinkRemaining <= 0) {
                _pet.isBlinking = false;
                _pet.blinkTimer = randomRange(DOG_BLINK_MIN_SEC, DOG_BLINK_MAX_SEC);
            }
        } else {
            _pet.blinkTimer -= dt;
            if (_pet.blinkTimer <= 0) {
                _pet.isBlinking = true;
                _pet.blinkRemaining = DOG_BLINK_DURATION_SEC;
            }
        }

        // Happy timer tick
        if (_pet.isHappy) {
            _pet.happyTimer -= dt;
            if (_pet.happyTimer <= 0) _pet.isHappy = false;
        }
    }

    // ── Behavior FSM ──
    switch (_pet.behavior) {
        case DogBehavior::NAP: {
            // Alternate NAP / SLEEP_Z
            _pet.napZTimer -= dt;
            if (_pet.napZTimer <= 0) {
                _pet.showingZ = !_pet.showingZ;
                _pet.napZTimer = DOG_NAP_Z_TOGGLE_SEC;
            }
            _pet.napRemaining -= dt;
            if (_pet.napRemaining <= 0) {
                _pet.behavior = DogBehavior::WANDER;
                _pet.phaseTimer = DOG_WANDER_DURATION_SEC;
                _pet.napTimer = DOG_NAP_INTERVAL_SEC;
                _pet.wanderTimer = randomRange(DOG_WANDER_PAUSE_MIN_SEC, DOG_WANDER_PAUSE_MAX_SEC);
            }
            break;
        }

        case DogBehavior::FOLLOW: {
            _pet.phaseTimer -= dt;
            _pet.targetPickTimer -= dt;

            if (_pet.phaseTimer <= 0) {
                _pet.behavior = DogBehavior::WANDER;
                _pet.phaseTimer = DOG_WANDER_DURATION_SEC;
                _pet.followTarget = -1;
                _pet.isSitting = false;
                _pet.wanderTimer = randomRange(DOG_WANDER_PAUSE_MIN_SEC, DOG_WANDER_PAUSE_MAX_SEC);
                break;
            }

            if (_pet.targetPickTimer <= 0) {
                petPickTarget();
                _pet.targetPickTimer = DOG_PICK_TARGET_SEC;
            }

            // Check if follow target is seated — dog sits nearby
            _pet.isSitting = false;
            if (_pet.followTarget >= 0 && _pet.followTarget < MAX_AGENTS && !_pet.walking) {
                const Character& target = _chars[_pet.followTarget];
                if (target.alive &&
                    (target.state == CharState::TYPE || target.state == CharState::READ)) {
                    int dx = abs(_pet.tileCol - target.tileCol);
                    int dy = abs(_pet.tileRow - target.tileRow);
                    if (dx + dy <= 2) {
                        _pet.isSitting = true;
                    }
                }
            }

            // Re-pathfind periodically while following (not while sitting)
            if (!_pet.walking && !_pet.isSitting) {
                _pet.repathTimer -= dt;
                if (_pet.repathTimer <= 0) {
                    petFollowNear();
                    _pet.repathTimer = DOG_FOLLOW_REPATHFIND_SEC;
                }
            }
            break;
        }

        case DogBehavior::WANDER: {
            _pet.phaseTimer -= dt;
            _pet.targetPickTimer -= dt;

            if (_pet.phaseTimer <= 0) {
                _pet.behavior = DogBehavior::FOLLOW;
                _pet.phaseTimer = DOG_FOLLOW_DURATION_SEC;
                if (_pet.followTarget < 0) petPickTarget();
                _pet.repathTimer = 0;
                break;
            }

            if (_pet.targetPickTimer <= 0) {
                petPickTarget();
                _pet.targetPickTimer = DOG_PICK_TARGET_SEC;
            }

            if (!_pet.walking) {
                _pet.wanderTimer -= dt;
                if (_pet.wanderTimer <= 0) {
                    // Random chance to show happy face when starting a new wander pause
                    if (!_pet.isHappy && randomRange(0, 1.0f) < DOG_HAPPY_CHANCE) {
                        _pet.isHappy = true;
                        _pet.happyTimer = DOG_HAPPY_DURATION_SEC;
                    }
                    petWander();
                    _pet.wanderTimer = randomRange(DOG_WANDER_MOVE_MIN_SEC, DOG_WANDER_MOVE_MAX_SEC);
                }
            }
            break;
        }
    }

    // ── Walk movement ──
    if (_pet.walking && _pet.pathIdx < _pet.pathLen) {
        if (_pet.frameTimer >= DOG_WALK_FRAME_DURATION_SEC) {
            _pet.frameTimer -= DOG_WALK_FRAME_DURATION_SEC;
            _pet.frame = (_pet.frame + 1) % 4;
        }

        PathNode next = _pet.path[_pet.pathIdx];

        int dc = next.col - _pet.tileCol;
        int dr = next.row - _pet.tileRow;
        if (dc > 0) _pet.dir = Dir::RIGHT;
        else if (dc < 0) _pet.dir = Dir::LEFT;
        else if (dr > 0) _pet.dir = Dir::DOWN;
        else _pet.dir = Dir::UP;

        _pet.moveProgress += (DOG_WALK_SPEED_PX_PER_SEC / TILE_SIZE) * dt;

        float fromX = _pet.tileCol * TILE_SIZE + TILE_SIZE / 2.0f;
        float fromY = _pet.tileRow * TILE_SIZE + TILE_SIZE / 2.0f;
        float toX = next.col * TILE_SIZE + TILE_SIZE / 2.0f;
        float toY = next.row * TILE_SIZE + TILE_SIZE / 2.0f;

        float t = _pet.moveProgress;
        if (t > 1.0f) t = 1.0f;
        _pet.x = fromX + (toX - fromX) * t;
        _pet.y = fromY + (toY - fromY) * t;

        if (_pet.moveProgress >= 1.0f) {
            _pet.tileCol = next.col;
            _pet.tileRow = next.row;
            _pet.x = toX;
            _pet.y = toY;
            _pet.pathIdx++;
            _pet.moveProgress = 0;
        }
    } else if (_pet.walking) {
        _pet.walking = false;
        _pet.frame = 0;
        _pet.frameTimer = 0;
        _pet.x = _pet.tileCol * TILE_SIZE + TILE_SIZE / 2.0f;
        _pet.y = _pet.tileRow * TILE_SIZE + TILE_SIZE / 2.0f;
    }
}

