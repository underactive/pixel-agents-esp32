// ── Reading tools (show reading animation instead of typing) ──
static bool isReadingTool(const char* tool) {
    if (!tool || tool[0] == '\0') return false;
    return (strcmp(tool, "Read") == 0 || strcmp(tool, "Grep") == 0 ||
            strcmp(tool, "Glob") == 0 || strcmp(tool, "WebFetch") == 0 ||
            strcmp(tool, "WebSearch") == 0);
}

void OfficeState::init() {
    memset(_chars, 0, sizeof(_chars));
    for (int i = 0; i < MAX_AGENTS; i++) {
        _chars[i].alive = false;
        _chars[i].seatIdx = -1;
        _chars[i].agentId = -1;
    }
    initTileMap();
    _connected = false;
    _lastHeartbeatMs = 0;
}

void OfficeState::spawnAllCharacters() {
    for (int i = 0; i < MAX_AGENTS; i++) {
        Character& ch = _chars[i];
        memset(&ch, 0, sizeof(Character));
        ch.alive = true;
        ch.id = (uint8_t)i;
        ch.palette = (uint8_t)(i % NUM_PALETTES);
        ch.state = CharState::IDLE;
        ch.isActive = false;
        ch.seatIdx = -1;
        ch.agentId = -1;
        ch.homeZone = (i < 3) ? SocialZone::BREAK_ROOM : SocialZone::LIBRARY;
        ch.dir = Dir::DOWN;
        ch.frame = 0;
        ch.frameTimer = 0;
        ch.wanderTimer = randomRange(WANDER_PAUSE_MIN_SEC, WANDER_PAUSE_MAX_SEC);
        ch.wanderCount = 0;
        ch.wanderLimit = (uint8_t)randomInt(WANDER_MOVES_MIN, WANDER_MOVES_MAX);
        ch.pathLen = 0;
        ch.pathIdx = 0;
        ch.bubbleType = 0;
        ch.bubbleTimer = 0;
        ch.effectTimer = 0;
        ch.toolName[0] = '\0';

        // Place at a random walkable tile within home zone
        int colMin, colMax, rowMin, rowMax;
        if (ch.homeZone == SocialZone::BREAK_ROOM) {
            colMin = ZONE_BREAK_COL_MIN; colMax = ZONE_BREAK_COL_MAX;
            rowMin = ZONE_BREAK_ROW_MIN; rowMax = ZONE_BREAK_ROW_MAX;
        } else {
            colMin = ZONE_LIB_COL_MIN; colMax = ZONE_LIB_COL_MAX;
            rowMin = ZONE_LIB_ROW_MIN; rowMax = ZONE_LIB_ROW_MAX;
        }

        bool placed = false;
        for (int attempt = 0; attempt < 40; attempt++) {
            int8_t col = (int8_t)randomInt(colMin, colMax);
            int8_t row = (int8_t)randomInt(rowMin, rowMax);
            if (isWalkable(col, row)) {
                ch.tileCol = col;
                ch.tileRow = row;
                ch.x = col * TILE_SIZE + TILE_SIZE / 2.0f;
                ch.y = row * TILE_SIZE + TILE_SIZE / 2.0f;
                placed = true;
                break;
            }
        }
        if (!placed) {
            // Fallback: scan zone for any walkable tile
            for (int r = rowMin; r <= rowMax && !placed; r++) {
                for (int c = colMin; c <= colMax && !placed; c++) {
                    if (isWalkable((int8_t)c, (int8_t)r)) {
                        ch.tileCol = (int8_t)c;
                        ch.tileRow = (int8_t)r;
                        ch.x = c * TILE_SIZE + TILE_SIZE / 2.0f;
                        ch.y = r * TILE_SIZE + TILE_SIZE / 2.0f;
                        placed = true;
                    }
                }
            }
        }
        if (!placed) {
            // Last resort: center of map
            ch.tileCol = 10;
            ch.tileRow = 3;
            ch.x = 10 * TILE_SIZE + TILE_SIZE / 2.0f;
            ch.y = 3 * TILE_SIZE + TILE_SIZE / 2.0f;
        }
    }

    initPet();
}

